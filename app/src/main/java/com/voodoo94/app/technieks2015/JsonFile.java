public class JsonFile {
    String name;
    String imgUrl;
    String desc;
    String regUrl;

    public String getName() {
        return this.name;
    }

    public String getImgUrl() {
        return this.imgUrl;
    }

    public String getDesc() {
        return this.desc;
    }

    public String getRegUrl() {
        return this.regUrl;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setImgUrl(String url) {
        this.imgUrl = url;
    }

    public void setDesc(String name) {
        this.desc = name;
    }

    public void setregUrl(String name) {
        this.regUrl = name;
    }
}
